

def convert_day_name(day_name):
    if(day_name == 'Monday'):
        return 'دوشنبه'
    elif(day_name == 'Tuesday'):
        return 'سه شنبه'
    elif(day_name == 'Wednesday'):
        return 'چهار شنبه'
    elif(day_name == 'Thursday'):
        return 'پنج شنبه'
    elif(day_name == 'Friday'):
        return 'جمعه'
    elif(day_name == 'Saturday'):
        return 'شنبه'
    elif(day_name == 'Sunday'):
        return 'یک شنبه'
    else:
        return 'نامشخص'
    
    





